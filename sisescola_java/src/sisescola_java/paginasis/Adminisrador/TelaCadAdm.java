    private void btInserirAdmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirAdmActionPerformed
        Adm adm = new Adm();
        AdmDAO dao = new AdmDAO();
        if (txtCPFAdm.getText().equals("")
            || txtNomeAdm.getText().equals("")
            || txtEmailAdm.getText().equals("")
            || txtSenhaAdm.getText().equals("")
            || txtDiaAdm.getText().equals("")
            || txtMesAdm.getText().equals("")
            || txtAnoAdm.getText().equals("")
            || txtEnderecoAdm.getText().equals("")
            || txtTelefoneAdm.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
            return;
            //||txtNomeA.getText().matches("^[a-zA-Z]+$")||txtEmailA.getText().matches("^[a-zA-Z]+$")||txtEmailA.getText().matches("^[a-zA-Z]+$")||txtSenhaA.getText().matches("^[a-zA-Z]+$")||txtDiaA.getText().matches("")||txtDiaA.getText().matches
        } else if (!txtCPFAdm.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do CPF deve conter apenas números.");
            return;
        } else if (!txtNomeAdm.getText().matches("^[a-zA-Z\\s]+$")) {
            JOptionPane.showMessageDialog(null, "O campo do nome deve conter apenas letras.");
            return;
        } else if (!txtDiaAdm.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do dia deve conter apenas números.");
            return;
        } else if (!txtMesAdm.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do mês deve conter apenas números.");
            return;
        } else if (!txtAnoAdm.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do ano deve conter apenas números.");
            return;
        } else if (!txtTelefoneAdm.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do telfone deve conter apenas números sem formatação.");
            return;
        } else if (txtCPFAdm.getText().length() != 11) {
            JOptionPane.showMessageDialog(null, "O CPF deve conter 11 números.");
            return;
        } else if (txtNomeAdm.getText().length() >= 80) {
            JOptionPane.showMessageDialog(null, "O Nome deve conter menos que 80 caracteres.");
            return;
        } else if (txtDiaAdm.getText().length() != 2) {
            JOptionPane.showMessageDialog(null, "O dia deve conter 2 números.");
            return;
        } else if (txtMesAdm.getText().length() != 2) {
            JOptionPane.showMessageDialog(null, "O mês deve conter 2 números.");
            return;
        } else if (txtAnoAdm.getText().length() != 4) {
            JOptionPane.showMessageDialog(null, "O ano deve conter 4 números.");
            return;
        } else if (txtTelefoneAdm.getText().length() != 11) {
            JOptionPane.showMessageDialog(null, "O telefone deve conter 11 números.");
            return;
        } else {
            adm.setCPFAdm(txtCPFAdm.getText());
        }
        adm.setNomeAdm(txtNomeAdm.getText());
        adm.setEmailAdm(txtEmailAdm.getText());
        adm.setSenhaAdm(txtSenhaAdm.getText());

        try {
            int dia = Integer.parseInt(txtDiaAdm.getText());
            int mes = Integer.parseInt(txtMesAdm.getText());
            int ano = Integer.parseInt(txtAnoAdm.getText());
            if (dia > 31 || dia == 0) {
                JOptionPane.showMessageDialog(null, "Dia inválido");
                return;
            } else if (mes > 12 || mes == 0) {
                JOptionPane.showMessageDialog(null, "Mês inválido");
                return;
            } else if (ano < 1944 || ano > 2024) {
                JOptionPane.showMessageDialog(null, "Ano inválido");
                return;
            } else {
                LocalDate data = LocalDate.of(ano, mes, dia);
                String dataFormatada = data.format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                adm.setData_NascAdm(dataFormatada);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Dia, mês e ano devem ser números válidos.");
            return;
        }
        adm.setEnderecoAdm(txtEnderecoAdm.getText());
        adm.setTelefoneAdm(txtTelefoneAdm.getText());

        adm.setGeneroAdm(boxGeneroAdm.getSelectedItem().toString());
        try {
            dao.cadastrarAdm(adm);
            JOptionPane.showMessageDialog(null, "Administrador cadastrado com sucesso!");
            Telalogin tl = new Telalogin();
            tl.setVisible(true);
            dispose();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaCadAdm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar administrador: Classe não encontrada.");
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadAdm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar administrador: SQL exception.");
        }
    }//GEN-LAST:event_btInserirAdmActionPerformed

