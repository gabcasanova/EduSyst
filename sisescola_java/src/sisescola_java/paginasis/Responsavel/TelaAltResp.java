    public void boxGeneros() {
        try {
            ResponsavelDAO rpd = new ResponsavelDAO();
            ResultSet rs = rpd.listarGeneros();

            while (rs.next()) {
                String genero = rs.getString("Genero");
                if (boxGeneroAResp.getItemCount() > 0 && !boxGeneroAResp.getItemAt(0).equals(genero)) {
                    boxGeneroAResp.addItem(genero);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na combobox do gênero: " + e);
        }
    }

    public void btconsulta(String pesquisa) {
        ResponsavelDAO rdao = new ResponsavelDAO();
        try {
            // Verifica se a string de pesquisa é um CPF (números com 11 dígitos)
            if (pesquisa.matches("\\d{11}")) {
                // Consulta pelo CPF
                for (Responsavel R : rdao.btconsultar(null, pesquisa)) {

                    txtCPFAResp.setText(R.getCPFResp());
                    txtNomeAResp.setText(R.getNomeResp());
                    txtEmailAResp.setText(R.getEmailResp());
                    txtSenhaAResp.setText("");
                    if (R.getData_NascResp() != null) {
                        LocalDate data = LocalDate.parse(R.getData_NascResp(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                        txtDiaAResp.setText(String.valueOf(data.getDayOfMonth()));
                        txtMesAResp.setText(String.valueOf(data.getMonthValue()));
                        txtAnoAResp.setText(String.valueOf(data.getYear()));
                    }
                    txtEnderecoAResp.setText(R.getEnderecoResp());
                    txtTelefoneAResp.setText(R.getTelefoneResp());
                    boxGeneroAResp.setSelectedItem(String.valueOf(R.getGeneroResp()));
                }
            } else {
                // Consulta pelo Nome
                for (Responsavel R : rdao.btconsultar(pesquisa, null)) {

                    txtCPFAResp.setText(R.getCPFResp());
                    txtNomeAResp.setText(R.getNomeResp());
                    txtEmailAResp.setText(R.getEmailResp());
                    txtSenhaAResp.setText("");
                    if (R.getData_NascResp() != null) {
                        LocalDate data = LocalDate.parse(R.getData_NascResp(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                        txtDiaAResp.setText(String.valueOf(data.getDayOfMonth()));
                        txtMesAResp.setText(String.valueOf(data.getMonthValue()));
                        txtAnoAResp.setText(String.valueOf(data.getYear()));
                    }
                    txtEnderecoAResp.setText(R.getEnderecoResp());
                    txtTelefoneAResp.setText(R.getTelefoneResp());
                    boxGeneroAResp.setSelectedItem(String.valueOf(R.getGeneroResp()));

                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(TelaConsResp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

