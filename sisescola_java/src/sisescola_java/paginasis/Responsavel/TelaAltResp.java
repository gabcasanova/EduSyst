    private void btAlterarRespActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarRespActionPerformed
        Responsavel r = new Responsavel();
        ResponsavelDAO dao = new ResponsavelDAO();
        if (txtCPFAResp.getText().equals("")
                || txtNomeAResp.getText().equals("")
                || txtEmailAResp.getText().equals("")
                || txtDiaAResp.getText().equals("")
                || txtMesAResp.getText().equals("")
                || txtAnoAResp.getText().equals("")
                || txtEnderecoAResp.getText().equals("")
                || txtTelefoneAResp.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
            return;
        } else if (!txtCPFAResp.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do CPF deve conter apenas números.");
            return;
        } else if (!txtNomeAResp.getText().matches("^[a-zA-Z\\s]+$")) {
            JOptionPane.showMessageDialog(null, "O campo do nome deve conter apenas letras.");
            return;
        } else if (!txtDiaAResp.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do dia deve conter apenas números.");
            return;
        } else if (!txtMesAResp.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do mês deve conter apenas números.");
            return;
        } else if (!txtAnoAResp.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do ano deve conter apenas números.");
            return;
        } else if (!txtTelefoneAResp.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do telefone deve conter apenas números sem formatação.");
            return;
        } else if (txtCPFAResp.getText().length() != 11) {
            JOptionPane.showMessageDialog(null, "O CPF deve conter 11 números.");
            return;
        } else if (txtNomeAResp.getText().length() >= 80) {
            JOptionPane.showMessageDialog(null, "O Nome deve conter menos que 80 caracteres.");
            return;
        } else if (txtDiaAResp.getText().length() > 2) {
            JOptionPane.showMessageDialog(null, "Dia inválido.");
            return;
        } else if (txtMesAResp.getText().length() > 2) {
            JOptionPane.showMessageDialog(null, "Mês inválido");
            return;
        } else if (txtAnoAResp.getText().length() != 4) {
            JOptionPane.showMessageDialog(null, "O ano deve conter 4 números.");
            return;
        } else if (txtTelefoneAResp.getText().length() != 11) {
            JOptionPane.showMessageDialog(null, "O telefone deve conter 11 números.");
            return;
        } else {
            r.setCPFResp(txtCPFAResp.getText());
        }

        r.setNomeResp(txtNomeAResp.getText());
        r.setEmailResp(txtEmailAResp.getText());
        r.setSenhaResp(txtSenhaAResp.getText());

        try {
            int dia = Integer.parseInt(txtDiaAResp.getText());
            int mes = Integer.parseInt(txtMesAResp.getText());
            int ano = Integer.parseInt(txtAnoAResp.getText());

            // Tentativa de criar a data diretamente
            try {
                LocalDate data = LocalDate.of(ano, mes, dia);
                String dataFormatada = data.format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                r.setData_NascResp(dataFormatada);  // Assumindo que r é o objeto Responsavel
            } catch (DateTimeException e) {
                // Caso a data seja inválida
                JOptionPane.showMessageDialog(null, "Data inválida: " + e.getMessage());
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Dia, mês e ano devem ser números válidos.");
            return;
        }
        r.setEnderecoResp(txtEnderecoAResp.getText());
        r.setTelefoneResp(txtTelefoneAResp.getText());
        r.setGeneroResp(boxGeneroAResp.getSelectedItem().toString());
        try {
            if (txtSenhaAResp.getText().isBlank() || txtSenhaAResp.getText().isEmpty()) {
                dao.AtualizarRespSemSenha(r);
            } else {
                dao.AtualizarRespComSenha(r);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaCadResp.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao atualizar aluno: Classe não encontrada.");
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadResp.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao atualizar aluno: SQL exception.");
        }
        txtCPFAResp.setText("");
        txtNomeAResp.setText("");
        txtEmailAResp.setText("");
        txtSenhaAResp.setText("");
        txtDiaAResp.setText("");
        txtMesAResp.setText("");
        txtAnoAResp.setText("");
        txtEnderecoAResp.setText("");
        txtTelefoneAResp.setText("");
        boxGeneroAResp.setSelectedIndex(0);
        txtPesquisarAltResp.setText("");
    }//GEN-LAST:event_btAlterarRespActionPerformed

