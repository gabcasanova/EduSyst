    private void btInserirAdmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirAdmActionPerformed
        Materias mat = new Materias();
        MateriasDAO dao = new MateriasDAO();
        if (txtNomeMat.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "O campo deve ser preenchido");
            return;
            //||txtNomeA.getText().matches("^[a-zA-Z]+$")||txtEmailA.getText().matches("^[a-zA-Z]+$")||txtEmailA.getText().matches("^[a-zA-Z]+$")||txtSenhaA.getText().matches("^[a-zA-Z]+$")||txtDiaA.getText().matches("")||txtDiaA.getText().matches
        } else {
            mat.setNome(txtNomeMat.getText());
        }
        try {
            dao.CriarMateria(mat);
            TelaConsMateria tcm = new TelaConsMateria();
            tcm.setVisible(true);
            dispose();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaCadAdm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar matéria: Classe não encontrada.");
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadAdm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar matéria: SQL exception.");
        }
    }//GEN-LAST:event_btInserirAdmActionPerformed

