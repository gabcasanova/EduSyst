    Vector<Integer> Id_Turma = new Vector<Integer>();

    private int getIdFromComboBox(JComboBox<String> comboBox, Vector<Integer> idList) {
        int index = comboBox.getSelectedIndex();
        if (index >= 0 && index < idList.size()) {
            return idList.get(index);
        }
        return -1; // Retorna -1 se não encontrar um ID válido
    }

    public void boxTurmas() {
        try {
            TurmaDAO Tdao = new TurmaDAO();
            ResultSet rs = Tdao.listarTurmas();

            boxIDT.removeAllItems(); // Limpa a JComboBox antes de adicionar novos itens
            Id_Turma.clear(); // Limpa o vetor de IDs

            while (rs.next()) {
                int idTurma = rs.getInt("Id_Turma");
                String Classe = rs.getString("Classe"); // Supondo que você tenha uma coluna nome_turma

                Id_Turma.addElement(idTurma);
                boxIDT.addItem(Classe);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na combobox das turmas: " + e);
        }
    }

    public void selecionarTurma(int idTurma) {
        for (int i = 0; i < Id_Turma.size(); i++) {
            if (Id_Turma.get(i) == idTurma) {
                boxIDT.setSelectedIndex(i);
                return;
            }
        }
        // Se não encontrar o ID, desmarcar a seleção
        boxIDT.setSelectedIndex(-1);
    }

    public void btconsulta(String pesquisa) {
        EnturmarDAO edao = new EnturmarDAO();
        try {
            Aluno aluno = edao.buscarAlunoPorCPFOuNome(pesquisa);
            if (aluno != null) {
                lblIDA.setText(String.valueOf(aluno.getId_Aluno()));
                lblNomeE.setText(aluno.getNomeA());
            } else {
                JOptionPane.showMessageDialog(this, "Aluno não encontrado.");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(TelaEnturmar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

