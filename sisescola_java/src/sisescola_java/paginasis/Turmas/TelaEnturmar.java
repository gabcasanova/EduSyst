    private void btEnturmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEnturmarActionPerformed
        Enturmar en = new Enturmar();
        EnturmarDAO DAO = new EnturmarDAO();

        // Verificação do ID do Aluno
        String alunoIDText = lblIDA.getText();
        if (alunoIDText == null || alunoIDText.trim().isEmpty() || alunoIDText.equals("ID do Aluno")) {
            JOptionPane.showMessageDialog(null, "ID do Aluno inválido.");
            return;
        }

        try {
            en.setAluno_ID(Integer.parseInt(alunoIDText));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID do Aluno não é um número válido.");
            return;
        }

        // Obtenção do ID da Turma
        int TurmaID = getIdFromComboBox(boxIDT, Id_Turma);
        if (TurmaID != -1) {
            en.setTurma_ID(TurmaID);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao obter ID da Turma.");
            return;
        }

        // Chamada para enturmar
        try {
            DAO.Enturmar(en);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao enturmar o aluno: " + e.getMessage());
        }
    }//GEN-LAST:event_btEnturmarActionPerformed

