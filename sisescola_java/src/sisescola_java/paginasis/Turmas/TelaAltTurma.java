    private void btCadTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadTurmaActionPerformed
        Turma t = new Turma();
        TurmaDAO tdao = new TurmaDAO();
        if (txtAnoT.getText().equals("")
                || txtClasseT.getText().equals("")
                || txtEtapaT.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
            return;
        } else if (!txtAnoT.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do Ano deve conter apenas números.");
            return;
        } else if (!txtClasseT.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo da Classe deve conter apenas números.");
            return;
        } else if (!txtEtapaT.getText().matches("^[\\p{L}\\s]+$")) {
            JOptionPane.showMessageDialog(null, "O campo da Etapa deve conter apenas letras.");
            return;
        } else if (txtAnoT.getText().length() != 4 && txtAnoT.getText().length() != 3) {
            JOptionPane.showMessageDialog(null, "O ano deve conter 4 números.");
            return;
        } else if (txtClasseT.getText().length() != 4 && txtClasseT.getText().length() != 3) {
            JOptionPane.showMessageDialog(null, "A classe deve conter 4 números.");
            return;
        }
        
        t.setAno(Integer.parseInt(txtAnoT.getText()));
        t.setClasse(Integer.parseInt(txtClasseT.getText()));
        t.setEtapa(txtEtapaT.getText());
        try {
            tdao.AtualizarTurma(t);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaCadTurma.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar aluno: Classe não encontrada.");
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadTurma.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar aluno: SQL exception.");
        }
        TelaConTurma tct = new TelaConTurma();
        tct.setVisible(true);
        dispose();
        
    }//GEN-LAST:event_btCadTurmaActionPerformed

