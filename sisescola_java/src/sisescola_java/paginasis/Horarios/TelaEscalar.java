    private void btEscalarProfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEscalarProfActionPerformed
        try {
            int selectedRow = tblProfessor.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Selecione um professor na tabela.");
                return;
            }

            // Pega o ID do professor selecionado
            int professorId = (int) tblProfessor.getValueAt(selectedRow, 0);

            // Pega o ID do horário selecionado na ComboBox
            int horarioId = getIdHorarioFromComboBox(comboBoxHorarios, Id_Horario);
            if (horarioId == -1) {
                JOptionPane.showMessageDialog(null, "Selecione um horário.");
                return;
            }

            // Cria a escala (associação entre professor e horário)
            Escalas escala = new Escalas();
            escala.setProfessor_ID(professorId);
            escala.setHorario_ID(horarioId);

            // Chama o método para inserir a escala
            EscalaDAO escalaDAO = new EscalaDAO();
            escalaDAO.Escalar(escala);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao escalar professor: " + e.getMessage());
        }
    }//GEN-LAST:event_btEscalarProfActionPerformed

