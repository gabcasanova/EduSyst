    private void carregarHorariosComboBox() throws ClassNotFoundException, SQLException {
        HorarioDAO horarioDAO = new HorarioDAO();
        List<Horario> horarios = horarioDAO.consultarTodosHorarios();

        comboBoxHorarios.removeAllItems(); // Limpa os itens da combo box
        Id_Horario.clear(); // Limpa o vetor de IDs

        // Adiciona os horários ao combo box
        for (Horario h : horarios) {
            Id_Horario.addElement(h.getId_Horario()); // Adiciona o ID ao vetor
            comboBoxHorarios.addItem(h.getTurno() + " - " + h.getDia_Semana() + " - "+ h.getInicio()); // Exibe o turno e o dia
        }
    }

    private int getIdHorarioFromComboBox(JComboBox<String> comboBox, Vector<Integer> idList) {
        int index = comboBox.getSelectedIndex();
        if (index >= 0 && index < idList.size()) {
            return idList.get(index); // Retorna o ID correspondente
        }
        return -1; // Retorna -1 se não encontrar um ID válido
    }

    private void buscarProfessor() {
        try {
            String pesquisa = txtBuscar.getText().trim();
            List<Professor> professores;

            ProfessorDAO professorDAO = new ProfessorDAO();

            // Determina se a pesquisa é por CPF ou nome
            if (pesquisa.matches("\\d+")) { // Verifica se a pesquisa é apenas números (assumindo que é CPF)
                professores = professorDAO.btconsultar(null, pesquisa);
            } else {
                professores = professorDAO.btconsultar(pesquisa, null);
            }

            // Preenche a JTable com os dados dos professores encontrados
            DefaultTableModel model = (DefaultTableModel) tblProfessor.getModel();
            model.setRowCount(0); // Limpa a tabela

            for (Professor p : professores) {
                model.addRow(new Object[]{p.getId_Professor(), p.getNomep(), p.getCPFp()});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar professor: " + e.getMessage());
        }
    }

