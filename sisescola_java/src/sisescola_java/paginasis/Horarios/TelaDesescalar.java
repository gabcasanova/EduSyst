    private void btDesescalarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDesescalarActionPerformed
        // Obtém a linha selecionada da tabela
        int selectedRow = tblProfessorEscalado.getSelectedRow();

        if (selectedRow != -1) {
            // Obtém os valores das células
            Object professorIdObj = tblProfessorEscalado.getValueAt(selectedRow, 0); // Coluna 0: ID_Professor
            Object nomeProfessorObj = tblProfessorEscalado.getValueAt(selectedRow, 2);  // Coluna 3: Nome
            Object horarioIdObj = tblProfessorEscalado.getValueAt(selectedRow, 3);    // Coluna 4: Horário

            // Imprime os valores para depuração
            System.out.println("professorIdObj: " + professorIdObj);
            System.out.println("nomeProfessorObj: " + nomeProfessorObj);
            System.out.println("horarioIdObj: " + horarioIdObj);

            int professorId = 0;
            int horarioId = 0;

            try {
                // Verifica e converte o ID do professor
                if (professorIdObj instanceof Integer) {
                    professorId = (int) professorIdObj;
                } else if (professorIdObj instanceof String) {
                    // Caso seja string (talvez o nome do professor), tente converter para número
                    try {
                        professorId = Integer.parseInt((String) professorIdObj);
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(this, "Erro: ID de professor inválido.");
                        return; // Sai do método em caso de erro
                    }
                }

                // Verifica e converte o ID do horário
                if (horarioIdObj instanceof Integer) {
                    horarioId = (int) horarioIdObj;
                } else if (horarioIdObj instanceof String) {
                    try {
                        // Imprime para depuração
                        System.out.println("Tentando converter horárioIdObj: " + horarioIdObj);
                        horarioId = Integer.parseInt((String) horarioIdObj);
                    } catch (NumberFormatException e) {
                        // Mostra o erro e retorna
                        JOptionPane.showMessageDialog(this, "Erro: ID de horário inválido.");
                        return;
                    }
                }

                // Verifica se o horarioId foi atribuído corretamente
                if (horarioId == 0) {
                    JOptionPane.showMessageDialog(this, "Erro: ID de horário inválido.");
                    return; // Retorna se o ID for inválido
                }

                // Chama o método para remover a escala
                EscalaDAO dao = new EscalaDAO();
                dao.removerEscala(professorId, horarioId);

                // Atualiza a tabela após a remoção
                DefaultTableModel modeloTabela = (DefaultTableModel) tblProfessorEscalado.getModel();
                modeloTabela.setRowCount(0); // Limpa a tabela

            } catch (ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Erro ao desescalar: " + e.getMessage());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Erro: ID de professor ou horário inválido.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um professor para desescalar.");
        }
    }//GEN-LAST:event_btDesescalarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cpfOuNome = txtBuscar.getText();  // Obtém o CPF ou nome do professor da interface
        DefaultTableModel modeloTabela = (DefaultTableModel) tblProfessorEscalado.getModel();
        modeloTabela.setRowCount(0); // Limpa a tabela antes de carregar os novos resultados

        String sql = "SELECT p.Id_Professor, p.Nome, p.CPF, h.Id_Horario, h.Inicio, h.Dia_Semana, h.Turno "
                + "FROM Professores p "
                + "INNER JOIN Professores_Horarios ph ON p.Id_Professor = ph.Professor_ID "
                + "INNER JOIN Horarios h ON ph.Horario_ID = h.Id_Horario "
                + "WHERE p.Nome LIKE ? OR p.CPF = ?";  // Pesquisa por nome ou CPF, sem ID de horário

        try (Connection con = ConnectionFactory.getConnection(); PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setString(1, "%" + cpfOuNome + "%");
            ps.setString(2, cpfOuNome);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                modeloTabela.addRow(new Object[]{
                    rs.getInt("Id_Professor"),
                    rs.getString("CPF"),
                    rs.getString("Nome"),
                    rs.getInt("h.Id_Horario"),
                    rs.getString("Inicio"),
                    rs.getString("Dia_Semana"),
                    rs.getString("Turno")
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro na consulta: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaDesescalar.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

