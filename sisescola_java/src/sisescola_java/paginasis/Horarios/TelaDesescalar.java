    private void carregarProfessoresHorarios() throws ClassNotFoundException {
        DefaultTableModel modeloTabela = (DefaultTableModel) tblProfessorEscalado.getModel();
        modeloTabela.setRowCount(0);  // Limpa a tabela antes de carregar os novos resultados

        // SQL para consultar os dados da tabela Professores_Horarios, incluindo o CPF do professor
        String sql = "SELECT p.Id_Professor, p.Nome, p.CPF, h.Id_Horario, h.Inicio, h.Dia_Semana, h.Turno "
                + "FROM Professores p "
                + "INNER JOIN Professores_Horarios ph ON p.Id_Professor = ph.Professor_ID "
                + "INNER JOIN Horarios h ON ph.Horario_ID = h.Id_Horario";

        try (Connection con = ConnectionFactory.getConnection(); PreparedStatement ps = con.prepareStatement(sql)) {

            ResultSet rs = ps.executeQuery(); // Executa a consulta SQL

            // Itera sobre o ResultSet e adiciona os resultados na tabela
            while (rs.next()) {
                // Depuração para verificar o que está sendo retornado do ResultSet
                System.out.println("ID Professor: " + rs.getInt("Id_Professor"));
                System.out.println("Nome: " + rs.getString("Nome"));
                System.out.println("CPF: " + rs.getString("CPF"));
                System.out.println("ID Horário: " + rs.getInt("Id_Horario"));
                System.out.println("Inicio: " + rs.getTime("Inicio"));
                System.out.println("Dia Semana: " + rs.getString("Dia_Semana"));
                System.out.println("Turno: " + rs.getString("Turno"));

                modeloTabela.addRow(new Object[]{
                    rs.getInt("Id_Professor"), // ID do professor
                    rs.getString("Nome"), // Nome do professor
                    rs.getString("CPF"), // CPF do professor
                    rs.getInt("Id_Horario"), // ID do horário
                    rs.getTime("Inicio"), // Horário de início
                    rs.getString("Dia_Semana"), // Dia da semana
                    rs.getString("Turno") // Turno
                });
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar horários: " + ex.getMessage());
        }
    }

