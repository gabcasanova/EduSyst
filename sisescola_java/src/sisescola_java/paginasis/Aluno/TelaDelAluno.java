    public void btconsulta(String pesquisa) {
        AlunoDAO adao = new AlunoDAO();
        try {
            // Verifica se a string de pesquisa é um CPF (números com 11 dígitos)
            if (pesquisa.matches("\\d{11}")) {
                // Consulta pelo CPF
                for (Aluno a : adao.btconsultar(null, pesquisa)) {

                    lblIDA.setText(String.valueOf(a.getId_Aluno()));
                    lblCPFA.setText(a.getCPFAluno());
                    lblNomeA.setText(a.getNomeA());
                    lblEmailA.setText(a.getEmailA());
                    lblNascA.setText(a.getData_NascA());
                    lblEnderecoA.setText(a.getEnderecoA());
                    lblTelefoneA.setText(a.getTelefoneA());
                    lblGeneroA.setText(a.getGeneroA());
                    lblResponsavelA.setText(a.getNomeResponsavel());
                }
            } else {
                // Consulta pelo Nome
                for (Aluno a : adao.btconsultar(pesquisa, null)) {

                    lblIDA.setText(String.valueOf(a.getId_Aluno()));
                    lblCPFA.setText(a.getCPFAluno());
                    lblNomeA.setText(a.getNomeA());
                    lblEmailA.setText(a.getEmailA());
                    lblNascA.setText(a.getData_NascA());
                    lblEnderecoA.setText(a.getEnderecoA());
                    lblTelefoneA.setText(a.getTelefoneA());
                    lblGeneroA.setText(a.getGeneroA());
                    lblResponsavelA.setText(a.getNomeResponsavel());
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(TelaConsAluno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

