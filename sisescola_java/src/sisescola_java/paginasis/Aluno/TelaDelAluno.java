    private void btDeletarAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletarAActionPerformed
        AlunoDAO A = new AlunoDAO();
        String nome = lblNomeA.getText();
        String cpf = lblCPFA.getText();

        if ((nome.isEmpty() || nome.equals("Nome")) && (cpf.isEmpty() || cpf.equals("CPF"))) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um nome ou CPF para exclusão.");
            return;
        }
        try {
            int i = JOptionPane.showConfirmDialog(null, "Deseja excluir este aluno? Essa ação é irreversível");
            if (i == JOptionPane.YES_OPTION) {
                A.Excluir(nome, cpf);
                A.btconsultar("", ""); // Atualiza a tabela após a exclusão
                lblIDA.setText("");
                lblCPFA.setText("");
                lblNomeA.setText("");
                lblEmailA.setText("");
                lblNascA.setText("");
                lblEnderecoA.setText("");
                lblTelefoneA.setText("");
                lblGeneroA.setText("");
                lblResponsavelA.setText("");
                txtNomeAlunoP.setText("");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(TelaConsAluno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btDeletarAActionPerformed

