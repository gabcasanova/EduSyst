    private void btInserirAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirAActionPerformed
        Aluno a = new Aluno();
        AlunoDAO dao = new AlunoDAO();
        if (txtCPFA.getText().equals("")
                || txtNomeA.getText().equals("")
                || txtEmailA.getText().equals("")
                || txtSenhaA.getText().equals("")
                || txtDiaA.getText().equals("")
                || txtMesA.getText().equals("")
                || txtAnoA.getText().equals("")
                || txtEnderecoA.getText().equals("")
                || txtTelefoneA.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
            return;
            //||txtNomeA.getText().matches("^[a-zA-Z]+$")||txtEmailA.getText().matches("^[a-zA-Z]+$")||txtEmailA.getText().matches("^[a-zA-Z]+$")||txtSenhaA.getText().matches("^[a-zA-Z]+$")||txtDiaA.getText().matches("")||txtDiaA.getText().matches
        } else if (!txtCPFA.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do CPF deve conter apenas números.");
            return;
        } else if (!txtNomeA.getText().matches("^[\\p{L}\\s]+$")) {
            JOptionPane.showMessageDialog(null, "O campo do nome deve conter apenas letras.");
            return;
        } else if (!txtDiaA.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do dia deve conter apenas números.");
            return;
        } else if (!txtMesA.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do mês deve conter apenas números.");
            return;
        } else if (!txtAnoA.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do ano deve conter apenas números.");
            return;
        } else if (txtCPFA.getText().length() != 11) {
            JOptionPane.showMessageDialog(null, "O CPF deve conter 11 números.");
            return;
        } else if (txtNomeA.getText().length() >= 80) {
            JOptionPane.showMessageDialog(null, "O Nome deve conter menos que 80 caracteres.");
            return;
        } else if (txtDiaA.getText().length() != 2) {
            JOptionPane.showMessageDialog(null, "O dia deve conter 2 números.");
            return;
        } else if (txtMesA.getText().length() != 2) {
            JOptionPane.showMessageDialog(null, "O mês deve conter 2 números.");
            return;
        } else if (txtAnoA.getText().length() != 4) {
            JOptionPane.showMessageDialog(null, "O ano deve conter 4 números.");
            return;
        } else if (!txtTelefoneA.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "O campo do telefone deve conter apenas números.");
            return;
        } else {
            a.setCPFAluno(txtCPFA.getText());
        }
        a.setNomeA(txtNomeA.getText());
        a.setEmailA(txtEmailA.getText());
        a.setSenhaA(txtSenhaA.getText());

        try {
            int dia = Integer.parseInt(txtDiaA.getText());
            int mes = Integer.parseInt(txtMesA.getText());
            int ano = Integer.parseInt(txtAnoA.getText());

            if (dia <= 0 || dia > 31) {
                JOptionPane.showMessageDialog(null, "Dia inválido. Deve estar entre 1 e 31.");
                return;
            }
            if (mes <= 0 || mes > 12) {
                JOptionPane.showMessageDialog(null, "Mês inválido. Deve estar entre 1 e 12.");
                return;
            }
            if (ano < 1900 || ano > LocalDate.now().getYear()) {
                JOptionPane.showMessageDialog(null, "Ano inválido. Deve estar entre 1900 e o ano atual.");
                return;
            }
            LocalDate data = LocalDate.of(ano, mes, dia);

            String dataFormatada = data.format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));
            a.setData_NascA(dataFormatada);

            System.out.println("Data de nascimento cadastrada: " + dataFormatada);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Dia, mês e ano devem ser números válidos.");
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "Data inválida.");
        }
        int idResponsavel = getIdFromComboBox(boxRespA, Id_Responsavel);

        if (idResponsavel != -1) {
            a.setId_Responsavel(idResponsavel);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao obter ID do responsável.");
            return;
        }

        a.setGeneroA(boxGeneroA.getSelectedItem().toString());
        a.setEnderecoA(txtEnderecoA.getText());
        a.setTelefoneA(txtTelefoneA.getText());
        try {
            dao.cadastrarAluno(a);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaCadAluno.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar aluno: Classe não encontrada.");
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadAluno.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar aluno: SQL exception.");
        }
        TelaConsAluno Tca = new TelaConsAluno();
        Tca.setVisible(true);
        dispose();
    }//GEN-LAST:event_btInserirAActionPerformed

