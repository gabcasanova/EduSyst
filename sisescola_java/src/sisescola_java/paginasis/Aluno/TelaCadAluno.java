    private int getIdFromComboBox(JComboBox<String> comboBox, Vector<Integer> idList) {
        int index = comboBox.getSelectedIndex();
        if (index >= 0 && index < idList.size()) {
            return idList.get(index);
        }
        return -1; // Retorna -1 se não encontrar um ID válido
    }

    public boolean alunoExists(String cpf) throws SQLException, ClassNotFoundException {
        String query = "SELECT COUNT(*) FROM Alunos WHERE CPF = ?";
        try (Connection con = ConnectionFactory.getConnection(); PreparedStatement stmt = con.prepareStatement(query)) {
            stmt.setString(1, cpf);
            try (ResultSet rs = stmt.executeQuery()) {
                if (rs.next()) {
                    return rs.getInt(1) > 0;
                }
            }
        }
        return false;
    }

    public void boxReps() {
        try {
            AlunoDAO ald = new AlunoDAO();
            ResultSet rs = ald.listarResponsaveis();

            if (rs == null) {
                JOptionPane.showMessageDialog(null, "Erro ao obter dados dos responsáveis.");
                return;
            }

            Id_Responsavel.clear(); // Limpa a lista existente
            boxRespA.removeAllItems(); // Limpa a combobox

            while (rs.next()) {
                int idResponsavel = rs.getInt("Id_Responsavel");
                String nomeResponsavel = rs.getString("Nome");
                Id_Responsavel.addElement(idResponsavel);
                boxRespA.addItem(nomeResponsavel);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na combobox do responsável: " + e);
        }
    }

    public void selecionarResponsavel(int idResponsavel) {
        for (int i = 0; i < Id_Responsavel.size(); i++) {
            if (Id_Responsavel.get(i) == idResponsavel) {
                boxRespA.setSelectedIndex(i);
                return;
            }
        }
        // Se não encontrar o ID, desmarcar a seleção
        boxRespA.setSelectedIndex(-1);
    }

    public void boxGeneros() {
        try {
            AlunoDAO ald = new AlunoDAO();
            ResultSet rs = ald.listarGeneros();

            while (rs.next()) {
                String genero = rs.getString("Genero");
                // Verifica se o gênero já está na JComboBox
                boolean existe = false;
                for (int i = 0; i < boxGeneroA.getItemCount(); i++) {
                    if (boxGeneroA.getItemAt(i).equals(genero)) {
                        existe = true;
                        break;
                    }
                }
                if (!existe) {
                    boxGeneroA.addItem(genero);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na combobox do gênero: " + e);
        }
    }

    public void selecionarGenero(String genero) {
        for (int i = 0; i < boxGeneroA.getItemCount(); i++) {
            if (boxGeneroA.getItemAt(i).equals(genero)) {
                boxGeneroA.setSelectedIndex(i);
                return;
            }
        }
        // Se não encontrar o gênero, desmarcar a seleção
        boxGeneroA.setSelectedIndex(-1);
    }

