    private void carregarProfessoresHorarios() throws ClassNotFoundException {
        DefaultTableModel modeloTabela = (DefaultTableModel) tblProfessorEscalado.getModel();
        modeloTabela.setRowCount(0);  // Limpa a tabela antes de carregar os novos resultados

        // SQL para consultar os dados da tabela Professores_Horarios, incluindo o CPF do professor
        String sql = "SELECT horarios.Dia_Semana, horarios.Turno, horarios.Inicio, horarios.Fim, "
                + "materias.Nome AS Nome_Materia, professores.Nome AS Nome_Professor, turmas.Classe, alunos.Nome "
                + "FROM Professores "
                + "INNER JOIN Professores_Horarios ON Professores.Id_Professor = Professores_Horarios.Professor_ID "
                + "INNER JOIN Horarios ON Horarios.Id_Horario = Professores_Horarios.Horario_ID "
                + "INNER JOIN Horarios_Materias ON Horarios.Id_Horario = Horarios_Materias.Horario_ID "
                + "INNER JOIN Materias ON Materias.Id_Materia = Horarios_Materias.Materia_ID "
                + "INNER JOIN Turmas ON Turmas.Id_Turma = Horarios_Materias.Turma_ID "
                + "INNER JOIN Turmas_Alunos ON Turmas_Alunos.Turma_ID = Turmas.Id_Turma "
                + "INNER JOIN Alunos ON Alunos.Id_Aluno = Turmas_Alunos.Aluno_ID ";

        try (Connection con = ConnectionFactory.getConnection(); PreparedStatement ps = con.prepareStatement(sql)) {

            ResultSet rs = ps.executeQuery(); // Executa a consulta SQL

            // Itera sobre o ResultSet e adiciona os resultados na tabela
            while (rs.next()) {

                modeloTabela.addRow(new Object[]{
                    rs.getInt("Dia_Semana"), // ID do professor
                    rs.getString("Turno"), // Nome do professor
                    rs.getString("Inicio"), // CPF do professor
                    rs.getInt("Fim"), // ID do horário
                    rs.getTime("Nome_Materia"), // Horário de início
                    rs.getString("Nome_Professor"), // Dia da semana
                    rs.getString("Classe") // Turno
                });
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar horários: " + ex.getMessage());
        }
    }

