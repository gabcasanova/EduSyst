    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cpfOuNome = txtBuscar.getText();  // Obtém o CPF ou nome do aluno da interface
        DefaultTableModel modeloTabela = (DefaultTableModel) tblProfessorEscalado.getModel();
        modeloTabela.setRowCount(0); // Limpa a tabela antes de carregar os novos resultados

        String sql = "SELECT horarios.Dia_Semana, horarios.Turno, horarios.Inicio, horarios.Fim, "
                + "materias.Nome AS Nome_Materia, professores.Nome AS Nome_Professor, turmas.Classe, alunos.Nome "
                + "FROM Professores "
                + "INNER JOIN Professores_Horarios ON Professores.Id_Professor = Professores_Horarios.Professor_ID "
                + "INNER JOIN Horarios ON Horarios.Id_Horario = Professores_Horarios.Horario_ID "
                + "INNER JOIN Horarios_Materias ON Horarios.Id_Horario = Horarios_Materias.Horario_ID "
                + "INNER JOIN Materias ON Materias.Id_Materia = Horarios_Materias.Materia_ID "
                + "INNER JOIN Turmas ON Turmas.Id_Turma = Horarios_Materias.Turma_ID "
                + "INNER JOIN Turmas_Alunos ON Turmas_Alunos.Turma_ID = Turmas.Id_Turma "
                + "INNER JOIN Alunos ON Alunos.Id_Aluno = Turmas_Alunos.Aluno_ID "
                + "WHERE Alunos.CPF LIKE ? OR Alunos.Nome LIKE ?";

        try (Connection con = ConnectionFactory.getConnection(); PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, "%" + cpfOuNome + "%");  // Para busca parcial pelo CPF
            ps.setString(2, "%" + cpfOuNome + "%");  // Para busca parcial pelo Nome

            ResultSet rs = ps.executeQuery();

            boolean found = false;
            while (rs.next()) {
                if (!found) {
                    lblNomeA.setText(rs.getString("Nome")); // Define o nome do aluno na label
                    found = true; // Marca como encontrado para evitar redefinição
                }
                modeloTabela.addRow(new Object[]{
                    rs.getString("Dia_Semana"), // Dia da semana
                    rs.getString("Turno"), // Turno
                    rs.getTime("Inicio"), // Horário de início
                    rs.getTime("Fim"), // Horário de fim
                    rs.getString("Nome_Materia"), // Nome da matéria
                    rs.getString("Nome_Professor"), // Nome do professor
                    rs.getString("Classe") // Classe
                });
            }

            if (!found) {
                lblNomeA.setText("Aluno não encontrado."); // Exibe mensagem se não encontrar o aluno
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro na consulta: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaGradeAluno.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

