    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if (txtCPFAdm.getText().equals("") || snhUsuario.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.");
            return;
        }

        try {
            // Obtém o CPF e a senha
            String CPFadmin = txtCPFAdm.getText(); // Remove formatação, se necessário
            char[] Senha = snhUsuario.getPassword();
            String ValorSenha = new String(Senha);

            // Cria um objeto Adm e configura CPF e senha
            Adm admin = new Adm();
            admin.setCPFAdm(CPFadmin);
            admin.setSenhaAdm(ValorSenha);

            // Autentica o administrador
            AdmDAO DAOA = new AdmDAO();
            ResultSet rs = DAOA.Autenticaradm(admin);

            if (rs.next()) {
                // Se a autenticação for bem-sucedida, abre a tela de CRUD e fecha a tela de login
                TelaCRUDS tlc = new TelaCRUDS();
                tlc.setVisible(true);
                dispose();
            } else {
                // Caso contrário, mostra uma mensagem de erro
                JOptionPane.showMessageDialog(null, "Usuário ou Senha inválidos.");
            }
        } catch (SQLException e) {
            // Mostra mensagem de erro em caso de exceção
            JOptionPane.showMessageDialog(null, "Erro ao autenticar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

