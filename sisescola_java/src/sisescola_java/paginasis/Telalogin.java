    private void MostrarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarSenhaActionPerformed
        // Verifica o estado atual do botão para alternar entre mostrar e esconder
        if (MostrarSenha.getIcon().equals(iconeVer)) {
            // Torna a senha visível
            snhUsuario.setEchoChar((char) 0); // Mostra a senha
            MostrarSenha.setIcon(iconeEsconder); // Altera o texto do botão
        } else {
            // Torna a senha oculta
            snhUsuario.setEchoChar('•'); // Oculta a senha
            MostrarSenha.setIcon(iconeVer); // Altera o texto do botão
        }
    }//GEN-LAST:event_MostrarSenhaActionPerformed

